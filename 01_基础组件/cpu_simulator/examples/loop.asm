# 循环程序
# 这个程序计算1到10的和

# 初始化计数器 R1 = 1
LOAD R1, 1

# 初始化上限 R2 = 10
LOAD R2, 10

# 初始化累加器 R3 = 0
LOAD R3, 0

# 循环开始
# 标签: LOOP (地址: 12)

# 将当前计数器值加到累加器
ADD R3, R3, R1

# 计数器加1
LOAD R4, 1
ADD R1, R1, R4

# 比较计数器和上限
JUMP_GT R1, R2, 36  # 如果 R1 > R2，跳转到 DONE

# 跳回循环开始
JUMP 12

# 循环结束
# 标签: DONE (地址: 36)

# 将结果存储到内存
STORE R3, 100

# 停止执行
HALT 